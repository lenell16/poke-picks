export const decimetresToFeet = (decimetres: number) => {
  return decimetres / 3.048;
};

export const decimetresToMetres = (decimetres: number) => {
  return decimetres / 10;
};

export const hectogramsToPounds = (hectograms: number) => {
  return hectograms / 4.536;
};

export const hectogramsToKilograms = (hectograms: number) => {
  return hectograms / 10;
};
